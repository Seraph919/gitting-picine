#include <unistd.h>

int	main(int a, char **b)
{
	int	i;
	int	n;

	i = 0;
	n = 1;
	while (**b && n < a)
	{
		i = 0;
		while (b[n][i])
		{
			write(1, &b[n][i], 1);
			i++;
		}
		write(1, "\n", 1);
		n++;
	}
	return (0);
}
