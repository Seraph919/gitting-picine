#include <unistd.h>

int	main(int a, char **b)
{
	int	c;
	int	i;

	c = a - 1;
	while (c > 0)
	{
		i = 0;
		while (b[c][i])
		{
			write(1, &b[c][i], 1);
			i++;
		}
		write(1, "\n", 1);
		c--;
	}
	return (0);
}
